    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        BancoDAO iBanco = new BancoDAO();
        ArrayList<Bancos> listaBancos = iBanco.listar(input_buscar.getText());

        model.setRowCount(0); //esto resetea a 0 la tabla para poder 

        Object[] row = new Object[3];

        for (int i = 0; i < listaBancos.size(); i++) {
            row[0] = listaBancos.get(i).getBAN_ID_BANCO();
            row[1] = listaBancos.get(i).getCODIGO_BANCO();
            row[2] = listaBancos.get(i).getBAN_DESCRIPCION();

            model.addRow(row);
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed

        if (table_banco.getSelectedRowCount() == 1) {
            String id = String.valueOf(table_banco.getValueAt(table_banco.getSelectedRow(), 0));
            String codigo = String.valueOf(table_banco.getValueAt(table_banco.getSelectedRow(), 1));
            String nombre = String.valueOf(table_banco.getValueAt(table_banco.getSelectedRow(), 2));

            input_id_banco.setText(id);
            input_nombre_banco.setText(nombre);
            input_codigo_banco.setText(codigo);

        } else {
            JOptionPane.showMessageDialog(null, "Primero marque un elemento y luego presione editar ", "Se requiere acción previa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_editarActionPerformed

