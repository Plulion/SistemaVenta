    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        Bancos banco;

        if ("".equals(input_id_banco.getText())) {
            banco = new Bancos(
                    input_nombre_banco.getText(),
                    input_codigo_banco.getText(),
                    radio_button_activo.isSelected());

        } else {
            banco = new Bancos(
                    Integer.parseInt(input_id_banco.getText()),
                    input_nombre_banco.getText(),
                    input_codigo_banco.getText(),
                    radio_button_activo.isSelected()
            );

        }

        BancoDAO iBanco = new BancoDAO();

        iBanco.agregar(banco);

        //se limpian los inputs
        input_nombre_banco.setText("");
        input_id_banco.setText("");
        input_codigo_banco.setText("");
        radio_button_activo.setSelected(true);

        //se retea la tabla
        iBanco.obtenerTodos(table_banco, model);

    }//GEN-LAST:event_btn_guardarActionPerformed

