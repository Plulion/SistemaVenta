    private void Boton_Guardar_ComunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_Guardar_ComunasActionPerformed

        Comunas comuna;

        if ("".equals(ID_Comuna.getText())) {
            comuna = new Comunas(
                    Nombre_Comunas.getText(),
                    Codigo_Comunas.getText(),
                    Boton_Activo_Comunas.isSelected());

        } else {
            comuna = new Comunas(
                    Integer.parseInt(ID_Comuna.getText()),
                    Nombre_Comunas.getText(),
                    Codigo_Comunas.getText(),
                    Boton_Activo_Comunas.isSelected()
            );

        }

        ComunaDAO iComuna = new ComunaDAO();

        iComuna.agregar(comuna);

        //se limpian los inputs
        Nombre_Comunas.setText("");
        ID_Comuna.setText("");
        Codigo_Comunas.setText("");
        Boton_Activo_Comunas.setSelected(true);

        //se retea la tabla
        iComuna.obtenerTodos(Tabla_Comunas, model);

    }//GEN-LAST:event_Boton_Guardar_ComunasActionPerformed

    private void Boton_Editar_ComunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_Editar_ComunasActionPerformed
            if (Tabla_Comunas.getSelectedRowCount() == 1) {
            String ID = String.valueOf(Tabla_Comunas.getValueAt(Tabla_Comunas.getSelectedRow(), 0));
            String Codigo = String.valueOf(Tabla_Comunas.getValueAt(Tabla_Comunas.getSelectedRow(), 1));
            String Nombre = String.valueOf(Tabla_Comunas.getValueAt(Tabla_Comunas.getSelectedRow(), 2));

            ID_Comuna.setText(ID);
            Nombre_Comunas.setText(Nombre);
            Codigo_Comunas.setText(Codigo);

        } else {
            JOptionPane.showMessageDialog(null, "Primero marque un elemento y luego presione editar ", "Se requiere acción previa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Boton_Editar_ComunasActionPerformed

