    private void Boton_Buscar_ComunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_Buscar_ComunasActionPerformed
        ComunaDAO iComuna = new ComunaDAO();
        ArrayList<Comunas> listaComunas = iComuna.listar(Buscar_Comunas.getText());

        model.setRowCount(0); //esto resetea a 0 la tabla para poder 

        Object[] row = new Object[3];

        for (int i = 0; i < listaComunas.size(); i++) {
            row[0] = listaComunas.get(i).getIdComuna();
            row[1] = listaComunas.get(i).getDescripcionComuna();
            row[2] = listaComunas.get(i).getCodigoComuna();


            model.addRow(row);
        }
    }//GEN-LAST:event_Boton_Buscar_ComunasActionPerformed


